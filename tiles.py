SANGENPAI = ['5z', '6z', '7z']

# 바람패
KAZEPAI = ['1z', '2z', '3z', '4z']

def tile_to_emoji(tile: str) -> str:
    return TILE_EMOJI.get(tile, f'[{tile}]')

def tiles_to_str(tiles: list) -> str:
    return ''.join(tile_to_emoji(t) for t in tiles)

def is_terminal(tile: str) -> bool:
    """노두패 여부 (1,9)"""
    if tile[1] == 'z':
        return False
    return tile[0] in ('1', '9')

def is_honor(tile: str) -> bool:
    """자패 여부"""
    return tile[1] == 'z'

def is_terminal_or_honor(tile: str) -> bool:
    return is_terminal(tile) or is_honor(tile)

def is_green(tile: str) -> bool:
    """녹일색 구성 가능 패"""
    return tile in ['2s', '3s', '4s', '6s', '8s', '6z']

def next_tile(tile: str) -> str:
    """다음 패 (도라 계산용)"""
    suit = tile[1]
    num = int(tile[0])
    if suit == 'z':
        if num <= 4:  # 바람패: 동남서북 → 동
            return f'{(num % 4) + 1}z'
        else:         # 삼원패: 백발중 → 백
            return f'{((num - 5) % 3) + 5}z'
    else:
        return f'{(num % 9) + 1}{suit}'